def suma(a, b):
    mini_suma = a + b
    return mini_suma

def resta(a, b):
    mini_resta = a - b
    return mini_resta

def multiplicacion (a, b):
    mini_multiplicacion = a * b
    return mini_multiplicacion

def division (a, b):
    mini_division = a / b
    return mini_division

def potenciacion(a, b):
    mini_potenciacion = a ** b
    return mini_potenciacion

def radicacion(a, b):
    mini_potenciacion = pow(a, (1/b))
    return mini_potenciacion